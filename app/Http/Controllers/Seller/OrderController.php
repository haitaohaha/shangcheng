        // echo '11122121';

         $data = Goods::join('seller','goods.sid','=','seller.sid')
        ->where('order.sid',session('master')->sid)
        ->join('order','Goods.sid','=','order.sid')
        ->join('user','user.uid','=','order.uid')
        ->orderBy('oid','asc')
        ->paginate(5);

        // dd($data);
        return view('seller.order.index',['data'=>$data]);
